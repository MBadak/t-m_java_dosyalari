package day14dowhileloop;

public class DoWhile01 {

	public static void main(String[] args) {
		
		int num=1;
		
		do {
			System.out.println(num);
			num++;
		}while (num<4);
		
// 10 ile 20 arasýndaki tek tam sayýlarý ekrana  doWhile ileyazdýrýn
		
		int num1=11;
		
		do {
			System.out.print(num1+" ");
			num1+=2;
		}while (num1<20);
		
		
		
	}

}
