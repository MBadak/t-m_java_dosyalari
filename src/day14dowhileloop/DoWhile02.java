package day14dowhileloop;

public class DoWhile02 {

	public static void main(String[] args) {
		// do-while   ile  while  kotlarýnýn farký nedir.
		
		int num1=6;
		while (num1<7) {
			System.out.println(num1);
			num1++;
		}
		
		int num2=9;  // hatalýda olsa en az bir kez çalýþýr.
		
		do {
			System.out.println(num2);
			num2++;
		}while(num2<7);
		
		
		
		
		
		
		
		
		
	}

}
