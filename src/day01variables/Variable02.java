package day01variables;

public class Variable02 {

	public static void main(String[] args) {

		char harf1 = 'a'; // a nýn deðeri 97
		char harf2 = 'b'; // b nin deðeri 98
		System.out.println(harf1); // syso yazýp Ctrl speys e basýnca otamatik System.out.println(); yazar
		System.out.println(harf2);

		System.out.println(harf1 + harf2); // a+b 97+98 olarak görüyor

		// ab nasýl yazýlýr
		System.out.print(harf1);
		System.out.println(harf2); // doðru yaptým :)

		// bir char ýn sayý deðeri ASCII tablosu olamdan nasýl bulunur?

		// 'a' 97 bir tam sayý
		int aCharDeger = 'a';
		System.out.println(aCharDeger);

		// '!' '%' '*' sayý deðerleri kaç bulunuz

		byte ünlemsayýDeðeri = '!';

		System.out.println(ünlemsayýDeðeri);

		byte yüzdeCharDeðeri = '%';

		System.out.println(yüzdeCharDeðeri);

		byte asteriksCharDeðeri = '*';

		System.out.println(asteriksCharDeðeri);

		// bulduk tamam

	}

}
