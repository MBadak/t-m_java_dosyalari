package day12scopewhileloop;

public class WhileLoopSoruları1 {

	public static void main(String[] args) {
		// While loop kullanarak 3 den 13 e kadar tum tek tamsayýlarý ekrana yazdýrýnýz.

		int i = 3;

		while (i < 14) {
			if (i % 2 == 1) {
				System.out.print(i + " ");

			}
			i++;

		}

	}

}
